myDate = new Date();
console.log(myDate); //Date object is used to represent a date and time in javascript.

console.log(myDate.getTime()); //getTime method is used to get the number of milliseconds since January 1, 1970.

console.log(myDate.getFullYear()); //getFullYear method is used to get the year of a date.

console.log(myDate.getMonth()); //getMonth method is used to get the month of a date.

console.log(myDate.getDate()); //getDate method is used to get the day of a date.

console.log(myDate.getDay()); //getDay method is used to get the day of the week of a date.

console.log(myDate.getHours()); //getHours method is used to get the hours of a date.

console.log(myDate.getMinutes()); //getMinutes method is used to get the minutes of a date.

console.log(myDate.getSeconds()); //getSeconds method is used to get the seconds of a date.

console.log(myDate.getSeconds()); //getSeconds method is used to get the seconds of a date.